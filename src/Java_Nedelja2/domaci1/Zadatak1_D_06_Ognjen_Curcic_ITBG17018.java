package nedeljaII.domaci1;

import java.util.Scanner;

public class Zadatak1_D_06_Ognjen_Curcic_ITBG17018 {

    /*Zadatak 1:
    Napraviti program gde cete uneti neki broj ponavljanja i tekst koji ce se toliko puta ispisati.*/

    public static void main(String[] args) {

        Scanner sc =new Scanner(System.in);
        int brojPonavljanja = sc.nextInt();
        int i = 0;

        while (brojPonavljanja > i) {
            System.out.println("Polozicu ispit iz Jave!");
            i++;
        }

    }

}
